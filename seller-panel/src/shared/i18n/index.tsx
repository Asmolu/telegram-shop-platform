interface I18nContextValue {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: string, params?: TranslationParams) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => getStoredLanguage());

  useEffect(() => {
    document.documentElement.lang = language;
    localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
  }, [language]);

  useEffect(() => {
    const root = document.getElementById('root');
    if (!root) {
      return undefined;
    }

    const textMap = buildStaticTextMap(language);
    translateStaticText(root, textMap);

    const observer = new MutationObserver((mutations) => {
      for (const mutation of mutations) {
        if (mutation.type === 'characterData' && mutation.target.parentElement) {
          translateTextNode(mutation.target, textMap);
          continue;
        }

        if (mutation.type === 'attributes' && mutation.target instanceof HTMLElement) {
          translateElementAttributes(mutation.target, textMap);
          continue;
        }

        mutation.addedNodes.forEach((node) => {
          translateStaticText(node, textMap);
        });
      }
    });

    observer.observe(root, {
      attributes: true,
      attributeFilter: ['aria-label', 'placeholder', 'title'],
      characterData: true,
      childList: true,
      subtree: true,
    });

    return () => observer.disconnect();
  }, [language]);

  const setLanguage = useCallback((nextLanguage: Language) => {
    setLanguageState(nextLanguage);
  }, []);

  const t = useCallback(
    (key: string, params?: TranslationParams) => {
      const template = dictionaries[language][key] ?? dictionaries.en[key] ?? key;
      if (!params) {
        return template;
      }
      return template.replace(/\{(\w+)\}/g, (_, paramKey: string) =>
        params[paramKey] === undefined || params[paramKey] === null ? '' : String(params[paramKey]),
      );
    },
    [language],
  );

  const value = useMemo(() => ({ language, setLanguage, t }), [language, setLanguage, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used inside I18nProvider');
  }
  return context;
}

export function labelForEnum(value: string | null | undefined, t: I18nContextValue['t']) {
  if (!value) {
    return t('common.none');
  }
  return t(`enum.${value}`);
}

export function languageToLocale(language: Language): string {
  return language === 'ru' ? 'ru-RU' : 'en-US';
}

function getStoredLanguage(): Language {
  try {
    const stored = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return stored === 'en' || stored === 'ru' ? stored : 'ru';
  } catch {
    return 'ru';
  }
}

function buildStaticTextMap(language: Language): Map<string, string> {
  const sourceLanguage: Language = language === 'ru' ? 'en' : 'ru';
  const targetLanguage: Language = language;
  const map = new Map<string, string>();

  Object.keys(dictionaries.ru).forEach((key) => {
    const source = dictionaries[sourceLanguage][key];
    const target = dictionaries[targetLanguage][key];
    if (source && target && source !== target && !source.includes('{')) {
      map.set(source, target);
    }
  });

  return map;
}

function translateStaticText(node: Node, textMap: Map<string, string>) {
  if (node.nodeType === Node.TEXT_NODE) {
    translateTextNode(node, textMap);
    return;
  }

  if (!(node instanceof HTMLElement)) {
    return;
  }

  translateElementAttributes(node, textMap);

  const walker = document.createTreeWalker(node, NodeFilter.SHOW_TEXT);
  let currentNode = walker.nextNode();
  while (currentNode) {
    translateTextNode(currentNode, textMap);
    currentNode = walker.nextNode();
  }
}

function translateTextNode(node: Node, textMap: Map<string, string>) {
  const currentText = node.textContent ?? '';
  const translated = translateStaticValue(currentText, textMap);
  if (translated !== currentText) {
    node.textContent = translated;
  }
}

function translateElementAttributes(element: HTMLElement, textMap: Map<string, string>) {
  ['aria-label', 'placeholder', 'title'].forEach((attributeName) => {
    const value = element.getAttribute(attributeName);
    if (!value) {
      return;
    }

    const translated = translateStaticValue(value, textMap);
    if (translated !== value) {
      element.setAttribute(attributeName, translated);
    }
  });
}

function translateStaticValue(value: string, textMap: Map<string, string>) {
  const leading = value.match(/^\s*/)?.[0] ?? '';
  const trailing = value.match(/\s*$/)?.[0] ?? '';
  const trimmed = value.trim();
  const translated = textMap.get(trimmed);
  return translated ? `${leading}${translated}${trailing}` : value;
}
